package dto;

public class ProjectInput {
	
	//Private variables
	private int projectInputId;
    private int projectId;
    private int inputId;
    
    //Getters and setters
	public int getProjectInputId() {
		return projectInputId;
	}
	public void setProjectInputId(int projectInputId) {
		this.projectInputId = projectInputId;
	}
	public int getProjectId() {
		return projectId;
	}
	public void setProjectId(int projectId) {
		this.projectId = projectId;
	}
	public int getInputId() {
		return inputId;
	}
	public void setInputId(int inputId) {
		this.inputId = inputId;
	}
    
    
}
