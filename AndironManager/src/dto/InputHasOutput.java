package dto;

public class InputHasOutput {

	//Private variables
	private int inputHasOutputId;
	private int outputId;
	private int inputId;
	
	//Getters and Setters
	public int getInputHasOutputId() {
		return inputHasOutputId;
	}
	public void setInputHasOutputId(int inputHasOutputId) {
		this.inputHasOutputId = inputHasOutputId;
	}
	public int getOutputId() {
		return outputId;
	}
	public void setOutputId(int outputId) {
		this.outputId = outputId;
	}
	public int getInputId() {
		return inputId;
	}
	public void setInputId(int inputId) {
		this.inputId = inputId;
	}
	
	
}
